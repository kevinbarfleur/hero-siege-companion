def _parse_stat_defs(content: str) -> list[dict]:
    """Extract stat definitions array from JS chunk."""
    idx = content.find('[{"Stat ID":"total_damage_percent"')
    if idx < 0:
        return []
    depth = 0
    for i in range(idx, min(idx + 100000, len(content))):
        if content[i] == '[':
            depth += 1
        elif content[i] == ']':
            depth -= 1
            if depth == 0:
                try:
                    return json.loads(content[idx:i + 1])
                except json.JSONDecodeError:
                    return []
    return []


def _parse_items_from_js(content: str) -> list[dict]:
    """Extract all item objects from JS bundle."""
    pattern = re.compile(r'\{id:"[^"]+",name:"[^"]+",variants:\[')
    matches = list(pattern.finditer(content))

    items = []
    for m in matches:
        block = _extract_balanced_block(content, m.start())
        if not block:
            continue
        try:
            json_str = _js_obj_to_json(block)
            raw = json.loads(json_str)
        except json.JSONDecodeError:
            continue

        # Simplify: extract most recent version metadata + stats
        for variant in raw.get('variants', []):
            versions = variant.get('versions', [])
            if not versions:
                continue
            ver = versions[0]  # Most recent version
            meta = ver.get('metadata', {})
            name = meta.get('Item Name', raw.get('name', ''))
            if not name:
                continue

            stats = []
            for s in ver.get('stats', []):
                stat = {'id': s.get('Stat ID', '')}
                if 'Min Value1' in s:
                    stat['min'] = s['Min Value1']
                if 'Max Value1' in s:
                    stat['max'] = s['Max Value1']
                if 'Spell Name' in s:
                    stat['spell'] = s['Spell Name']
                if 'Class Name' in s:
                    stat['class'] = s['Class Name']
                if stat['id']:
                    stats.append(stat)

            items.append({
                'id': raw.get('id', ''),
                'name': name,
                'type': meta.get('Item Type', ''),
                'rarity': meta.get('Item Rarity', ''),
                'level': meta.get('Item Level Requirement', 0),
                'tier': meta.get('Item Tier', ''),
                'stats': stats,
            })

    return items


def fetch_items() -> dict | None:
    """
    Fetch and parse all items + stat definitions from hero-siege-helper.

    Returns:
    {
        "items": [...],     # Simplified item list
        "stat_defs": [...]  # Stat ID → display format mapping
    }
    """
    logger = logging.getLogger(LOGGING_NAME)

    content = _find_item_data_chunk()
    if not content:
        logger.error("Could not find item data chunk")
        return None

    items = _parse_items_from_js(content)
    logger.info(f"Parsed {len(items)} items from JS bundle")

    # Stat definitions (may be in a different chunk)
    stat_content = _find_stat_defs_chunk()
    stat_defs = _parse_stat_defs(stat_content) if stat_content else []
    logger.info(f"Parsed {len(stat_defs)} stat definitions")

    if not items:
        return None

    return {"items": items, "stat_defs": stat_defs}


